        public MatchResult Match(string input)
        {
            if (string.IsNullOrEmpty(input))
                return new MatchResult(false, 0, 0, -1);

            int current = _startState;
            int lastAcceptPos = -1;
            int lastAcceptId = -1;

            for (int i = 0; i < input.Length; i++)
            {
                int c = input[i];
                if (c >= 0 && c < 256)
                {
                    int next = _transitions[current][c];
                    if (next == -1)
                        break;
                    current = next;
                    if (_acceptStates[current] >= 0)
                    {
                        lastAcceptPos = i;
                        lastAcceptId = _acceptStates[current];
                    }
                }
                else
                {
                    break;
                }
            }

            if (lastAcceptPos >= 0)
                return new MatchResult(true, 0, lastAcceptPos + 1, lastAcceptId);

            return new MatchResult(false, 0, 0, -1);
        }

        public MatchResult MatchAt(string input, int startPos)
        {
            if (string.IsNullOrEmpty(input) || startPos < 0 || startPos >= input.Length)
                return new MatchResult(false, startPos, startPos, -1);

            int current = _startState;
            int lastAcceptPos = -1;
            int lastAcceptId = -1;

            for (int i = startPos; i < input.Length; i++)
            {
                int c = input[i];
                if (c >= 0 && c < 256)
                {
                    int next = _transitions[current][c];
                    if (next == -1)
                        break;
                    current = next;
                    if (_acceptStates[current] >= 0)
                    {
                        lastAcceptPos = i;
                        lastAcceptId = _acceptStates[current];
                    }
                }
                else
                {
                    break;
                }
            }

            if (lastAcceptPos >= 0)
                return new MatchResult(true, startPos, lastAcceptPos + 1, lastAcceptId);

            return new MatchResult(false, startPos, startPos, -1);
        }

